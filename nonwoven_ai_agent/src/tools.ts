export const storeAlertRecordTool = tool(
  async ({ tag, severity, message, value, unit }) => {
    try {
      const alertId = storeAlert({
        timestamp: new Date().toISOString(),
        tag,
        label: tag.replace(/_/g, " "),
        value,
        unit,
        level: severity,
        message,
      });
      return `Alert stored in database with ID ${alertId}`;
    } catch (error: any) {
      return `Failed to store alert: ${error.message}`;
    }
  },
  {
    name: "store_alert_record",
    description: "Store an alert record in the database for tracking and reporting.",
    schema: z.object({
      tag: z.string().describe("Machine tag identifier (e.g., EXTRUDER_AMP, WINDER_FAULT)"),
      severity: z.string().describe("Alert severity — CRITICAL, ALARM, WARNING, ANOMALY, or FAULT"),
      message: z.string().describe("Human-readable alert message"),
      value: z.number().describe("Current sensor value that triggered the alert"),
      unit: z.string().describe("Unit of measurement (A, RPM, %, m/min, etc.)"),
    }),
  }
);

export const queryTagHistoryTool = tool(
  async ({ tag_name, hours }) => {
    hours = Math.min(hours || 1, 24);
    try {
      const readings = getRecentReadings(tag_name, hours);
      if (readings.length === 0) {
        return `No readings found for ${tag_name} in the last ${hours} hour(s).`;
      }

      const values = readings.map(r => r.value);
      const sum = values.reduce((a, b) => a + b, 0);
      const summary = {
        tag: tag_name,
        period_hours: hours,
        sample_count: values.length,
        min: Number(Math.min(...values).toFixed(3)),
        max: Number(Math.max(...values).toFixed(3)),
        avg: Number((sum / values.length).toFixed(3)),
        latest: Number(values[values.length - 1].toFixed(3)),
        unit: readings[0].unit,
      };
      return JSON.stringify(summary);
    } catch (error: any) {
      return `Error querying history: ${error.message}`;
    }
  },
  {
    name: "query_tag_history",
    description: "Query recent sensor readings for a specific tag. Use this to get historical context.",
    schema: z.object({
      tag_name: z.string().describe("The tag to query (e.g., EXTRUDER_AMP, LAMINATOR_MPM)"),
      hours: z.number().optional().describe("How many hours of history to retrieve (default 1, max 24)"),
    }),
  }
);

export const getProductionStatsTool = tool(
  async ({ period }) => {
    try {
      let dateStr = period;
      if (period === "today" || !period) {
        dateStr = new Date().toISOString().split("T")[0];
      }
      const stats = getDailyStats(dateStr);
      if (Object.keys(stats).length === 0) {
        return `No production data available for ${dateStr}.`;
      }
      return JSON.stringify(stats, null, 2);
    } catch (error: any) {
      return `Error getting production stats: ${error.message}`;
    }
  },
  {
    name: "get_production_stats",
    description: "Get aggregated production statistics for a given period.",
    schema: z.object({
      period: z.string().describe("Either 'today' or 'YYYY-MM-DD' for a specific date"),
    }),
  }
);
