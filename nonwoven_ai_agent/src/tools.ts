import { tool } from "@langchain/core/tools";
import { z } from "zod";
import nodemailer from "nodemailer";
import { config } from "./config";
import { getRecentReadings, getDailyStats, storeAlert } from "./db";

// ── Nodemailer Transporter ────────────────────────────────────────

const transporter = nodemailer.createTransport({
  host: config.SMTP_HOST,
  port: config.SMTP_PORT,
  secure: config.SMTP_SECURE,
  auth: {
    user: config.SMTP_USER,
    pass: config.SMTP_PASS,
  },
});

function _wrapAlertHtml(bodyText: string, severity: string): string {
  const severityColors: any = {
    CRITICAL: "#dc2626",
    ALARM: "#ea580c",
    WARNING: "#d97706",
    ANOMALY: "#2563eb",
    FAULT: "#9333ea",
  };
  const color = severityColors[severity] || "#6b7280";

  return `
  <div style="font-family: Arial, sans-serif; max-width: 600px; margin: 0 auto;">
      <div style="background: ${color}; color: white; padding: 16px 24px; border-radius: 8px 8px 0 0;">
          <h2 style="margin: 0;">⚠️ ${severity} ALERT</h2>
          <p style="margin: 4px 0 0; opacity: 0.9;">Nonwoven Lamination Machine</p>
      </div>
      <div style="background: #f8f9fa; padding: 24px; border: 1px solid #e5e7eb; border-top: none; border-radius: 0 0 8px 8px;">
          <pre style="white-space: pre-wrap; font-family: monospace; font-size: 14px; line-height: 1.6;">${bodyText}</pre>
      </div>
      <p style="color: #9ca3af; font-size: 12px; text-align: center; margin-top: 12px;">
          Auto-generated by Nonwoven AI Agent — ${new Date().toISOString()}
      </p>
  </div>
  `;
}

// ── LangChain Tools ───────────────────────────────────────────────

export const sendAlertEmailTool = tool(
  async ({ subject, body, severity }) => {
    if (config.ALERT_RECIPIENTS.length === 0) {
      return "No alert recipients configured. Email not sent.";
    }
    
    try {
      const mailOptions: any = {
        from: `"${config.SENDER_NAME}" <${config.SENDER_EMAIL || config.SMTP_USER}>`,
        to: config.ALERT_RECIPIENTS.join(", "),
        subject: subject,
        text: body,
        html: _wrapAlertHtml(body, severity),
      };

      if (severity === "CRITICAL" || severity === "ALARM") {
        mailOptions.priority = "high";
      }

      await transporter.sendMail(mailOptions);
      return `Alert email sent successfully to ${config.ALERT_RECIPIENTS.join(", ")}`;
    } catch (error: any) {
      return `Failed to send email: ${error.message}`;
    }
  },
  {
    name: "send_alert_email",
    description: "Send an alert email to factory management.",
    schema: z.object({
      subject: z.string().describe("Email subject line including severity and component"),
      body: z.string().describe("Email body text describing the alert, current values, and recommended action"),
      severity: z.string().describe("Alert severity level — CRITICAL, ALARM, WARNING, or ANOMALY"),
    }),
  }
);

